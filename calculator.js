const calculator = {
  // add
  add(firstNumber, secondNumber) {
    return firstNumber + secondNumber;
  },
  // subtract
  subtract(firstNumber, secondNumber) {
    return firstNumber - secondNumber;
  },
  // divide
  divide(firstNumber, secondNumber) {
    return firstNumber / secondNumber;
  },
  // multiply
  multiply(firstNumber, secondNumber) {
    return firstNumber * secondNumber;
  },
};

module.exports = calculator;
